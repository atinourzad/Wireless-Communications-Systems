function [c,ceq] = avgPowerC(x)
    c = [];

    p1 = 2/(10^(x(1)/10));
    p2 = 6/(10^(x(2)/10));
    p3 = 28/(10^(x(3)/10));
    p4 = 120/(10^(x(4)/10));
    
    ceq = p1 * qfunc((x(1)-10)/8) + (p2-p1) * qfunc((x(2)-10)/8) + (p3-p2) * qfunc((x(3)-10)/8) + (p4-p3) * qfunc((x(4)-10)/8) - 1;
end

